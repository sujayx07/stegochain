# ── Private helpers ───────────────────────────────────────────────────────────

def _keccak256_bytes(data: bytes) -> bytes:
    """Return raw 32-byte keccak256 digest of *data*."""
    return Web3.keccak(data)


def _hash_pair(a: bytes, b: bytes) -> bytes:
    """Hash a sorted pair (smaller first) with keccak256."""
    if a <= b:
        return Web3.keccak(a + b)
    else:
        return Web3.keccak(b + a)


def _hex_to_bytes32(hex_str: str) -> bytes:
    """Convert a 0x-prefixed hex string to 32 raw bytes."""
    stripped = hex_str.lstrip("0x")
    return bytes.fromhex(stripped.zfill(64))


def _parse_record_tuple(rec) -> dict:
    """Convert a contract tuple/namedtuple to a plain dict."""
    return {
        "record_id":   rec[0],
        "cid":         rec[1],
        "sender":      rec[2],
        "receiver":    rec[3],
        "session_id":  rec[4],
        "merkle_root": "0x" + rec[5].hex(),
        "timestamp":   rec[6],
        "is_active":   rec[7],
    }


def _extract_record_id_from_receipt(w3: Web3, contract, receipt) -> int:
    """Parse the RecordRegistered event to get the recordId."""
    try:
        logs = contract.events.RecordRegistered().process_receipt(receipt)
        if logs:
            return logs[0]["args"]["recordId"]
    except Exception:
        pass
    # Fallback: recordId = recordCount - 1 after the tx
    return contract.functions.recordCount().call() - 1
